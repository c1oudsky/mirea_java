package com.company;

public class Circle {
    private float x0, y0, radius;
    public float GetX0() {
        return x0;
    }
    public float GetY0() {
        return y0;
    }
    public float GetRadius() {
        return radius;
    }
    public void SetX0(float a) {
        this.x0=a;
    }
    public void SetY0(float a) {
        this.y0=a;
    }
    public void SetRadius(float a) {
        this.radius=a;
    }

}
